--Q6. Find Top 5 States by Revenue?

select * from phonepe

select top 5 [state], round(sum(amount),2) as revenue
from phonepe
group by [state]
order by revenue desc

---------------------------------------------------------------------------------------------------

--Q7. Find Monthly Revenue Trend?

select * from phonepe

select 
datename(month,transaction_date) as [month],
round(sum(amount),2) as Revenue
from phonepe
group by
datename(month,transaction_date)
order by Revenue desc

---------------------------------------------------------------------------------------------------

--Q7. Find Monthly Transaction Trend?

select * from phonepe

select 
datename(month,transaction_date) as [month],
count(transaction_id) as Total_Transactions
from phonepe
group by
datename(month,transaction_date)
order by Total_Transactions desc

---------------------------------------------------------------------------------------------------

--Q8. Most Used Transaction Type?

select * from phonepe

select Transaction_type, 
count(Transaction_id) as total_Transaction
from phonepe
group by Transaction_type
order by total_Transaction desc

---------------------------------------------------------------------------------------------------

--Q9. Find Average Transaction Value by Payment Method?

select * from phonepe

select Payment_Method,
round(avg(amount),0) as Avg_Trans_Value
from phonepe
group by Payment_Method
order by Avg_Trans_Value desc

---------------------------------------------------------------------------------------------------

--Q10. Find Total Revenue Generated by Each Bank?

select * from phonepe

select Bank_name,
round(sum(amount),2) as Total_Revenue
from phonepe
group by Bank_name
order by Total_Revenue desc

---------------------------------------------------------------------------------------------------

--Q11.Find the Total no of transactions are Successed,Failed and Pending?

select * from phonepe

select transaction_status,
count(transaction_id) as Total_number
from phonepe
group by transaction_status
order by Total_number desc


--Q12. Find Failed Transactions Count State-wise

select * from phonepe

select [state],transaction_status,count(transaction_id) as Trans_not_done
from phonepe
where transaction_status <>'Success'
group by [state],transaction_status
order by [state],Trans_not_done desc

--or Total no of transactions are not done

select [state],count(transaction_id) as Trans_not_done
from phonepe
