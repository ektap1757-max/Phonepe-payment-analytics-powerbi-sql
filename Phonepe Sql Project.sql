--Q13. Find Top Merchant Categories by transactions?

select * from phonepe

select merchant_category,
count(transaction_id) as Number_of_transaction
from phonepe
group by merchant_category
order by Number_of_transaction desc

---------------------------------------------------------------------------------------------------

--Q14.Find Revenue Contribution by Gender?

select * from phonepe

select gender,
round(sum(amount),2) as total_revenue
from phonepe
group by gender
