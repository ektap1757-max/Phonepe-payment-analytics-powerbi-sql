---------------------------------------------------------------------------------------------------

--Q15. Which regions have low adoption but high engagement?

select * from phonepe

select [state],
sum(app_opens) as Total_App_Opens,
sum(registered_users) as Total_Registered_Users,
count(transaction_id) as Total_Transactions,
round(cast(sum(app_opens) as float)/ count(transaction_id),2) as Engagement_Ratio
from phonepe
group by [state]
order by Engagement_Ratio desc




